def normalize_locale(locale: str | None) -> LocaleCode:
    """Normalize locale values to the supported locale set."""
    if locale in SUPPORTED_LOCALES:
        return locale
    return DEFAULT_LOCALE


def get_user_locale() -> LocaleCode:
    """Read the user's language preference from settings."""
    import json

    from utils.constants.paths import DECK_SELECTOR_SETTINGS_FILE

    try:
        if DECK_SELECTOR_SETTINGS_FILE.exists():
            with open(DECK_SELECTOR_SETTINGS_FILE, encoding="utf-8") as f:
                settings = json.load(f)
            return normalize_locale(settings.get("language"))
    except Exception:
        pass
    return DEFAULT_LOCALE


def translate(locale: str | None, key: str, **kwargs: object) -> str:
    """Return a translated string with fallback to default locale and key text."""
    normalized = normalize_locale(locale)
    template = MESSAGES.get(normalized, {}).get(key) or MESSAGES[DEFAULT_LOCALE].get(key)
    if template is None:
        return key
    if not kwargs:
        return template
    return template.format(**kwargs)
